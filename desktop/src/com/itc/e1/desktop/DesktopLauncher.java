public class DesktopLauncher {
	public static void main (String[] arg) {
		LwjglApplicationConfiguration config = new LwjglApplicationConfiguration();
		new LwjglApplication(new MainClass(), config);
		config.width = 1100;
		config.height = 619;
		config.resizable = false;
	}
}
